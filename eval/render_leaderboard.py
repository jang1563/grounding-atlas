"""Render the GroundBench leaderboard as a self-contained, sortable HTML page (Phase C / C3).

Reads results/benchmark/<model>/scorecard.json and writes docs/leaderboard.html: one file (inline CSS +
JS, no external dependencies) with a sortable, web-tag-filterable, AUROC-heatmapped table. Host it on
GitHub Pages or a Hugging Face Space, or just open it locally. No API.

Run:  python eval/render_leaderboard.py
"""
import glob
import json
import os

HERE = os.path.dirname(os.path.abspath(__file__))
ROOT = os.path.dirname(HERE)
OUT = os.path.join(ROOT, "results", "benchmark")

TEMPLATE = """<!doctype html>
<html lang="en"><head><meta charset="utf-8"><meta name="viewport" content="width=device-width,initial-scale=1">
<title>GroundBench leaderboard</title>
<style>
:root{--bg:#0f1117;--fg:#e6e8ee;--mut:#9aa3b2;--line:#262b36;--rich:#1f6f43;--zero:#7a2230;--mixed:#7a5a1f;}
*{box-sizing:border-box}body{margin:0;background:var(--bg);color:var(--fg);
font:14px/1.5 -apple-system,BlinkMacSystemFont,"Segoe UI",Roboto,sans-serif}
.wrap{max-width:1100px;margin:0 auto;padding:28px 20px 60px}
h1{margin:0 0 4px;font-size:22px}.sub{color:var(--mut);margin:0 0 16px}
.note{background:#161a23;border:1px solid var(--line);border-radius:8px;padding:12px 14px;color:#c7cdda;
font-size:13px;margin:0 0 18px}
.controls{margin:0 0 12px}.controls button{background:#1a1f29;color:var(--fg);border:1px solid var(--line);
border-radius:999px;padding:5px 12px;margin-right:6px;cursor:pointer;font-size:13px}
.controls button.on{background:#2a3140;border-color:#46506a}
table{border-collapse:collapse;width:100%;font-variant-numeric:tabular-nums}
th,td{padding:7px 10px;text-align:right;border-bottom:1px solid var(--line);white-space:nowrap}
th:first-child,td:first-child{text-align:left}th{cursor:pointer;color:var(--mut);font-weight:600;
position:sticky;top:0;background:var(--bg);user-select:none}th:hover{color:var(--fg)}
td.task{font-family:ui-monospace,SFMono-Regular,Menlo,monospace;font-size:13px}
.pill{display:inline-block;padding:1px 8px;border-radius:999px;font-size:12px;color:#fff}
.rich{background:var(--rich)}.zero{background:var(--zero)}.mixed{background:var(--mixed)}
.ci{color:var(--mut);font-size:11px}.muted{color:var(--mut)}
footer{color:var(--mut);font-size:12px;margin-top:20px}a{color:#7aa2ff}
</style></head><body><div class="wrap">
<h1>GroundBench leaderboard</h1>
<p class="sub" id="sub"></p>
<div class="note"><b>Read the <code>web</code> tag as the snap-verbalization floor, not a single law.</b>
The name/anon gap is real and large, but its mechanism is a capability-dependent mix of
token-familiarity/reasoning and mapping-documentation (the familiarity share rises with capability). The
<code>cheap-head</code> column is a reproducible logistic-regression head on the same representation (the
orchestrate-via-a-trained-head reference), not a model. No single-number reduction.</div>
<div class="controls">filter web: <span id="filters"></span></div>
<table id="tbl"><thead><tr id="head"></tr></thead><tbody id="body"></tbody></table>
<footer>Generated by <code>eval/render_leaderboard.py</code>. AUROC heatmap: chance 0.5 neutral, higher
greener. Click a header to sort. See <code>docs/GROUNDBENCH.md</code> and
<code>docs/GROUNDBENCH_SPEC.md</code>.</footer>
</div>
<script>
const DATA = /*DATA*/;
const heat = v => { if(v==null) return 'transparent'; const t=Math.max(0,Math.min(1,(v-0.5)/0.5));
  return `rgba(${Math.round(120-80*t)},${Math.round(120+70*t)},${Math.round(120-40*t)},${0.10+0.5*t})`; };
let webFilter='all', sortKey='web', sortDir=1;
const fmt = v => v==null ? '<span class="muted">-</span>' : v.toFixed(3);
function rowsFor(){ let r = DATA.rows.filter(x=> webFilter==='all'||x.web===webFilter);
  const key=sortKey; r.sort((a,b)=>{ let av,bv;
    if(key==='task'){av=a.task;bv=b.task;return sortDir*(av<bv?-1:av>bv?1:0);}
    if(key==='web'){av=a.web;bv=b.web;return sortDir*(av<bv?-1:av>bv?1:0);}
    if(key==='ceiling'){av=a.ceiling??-1;bv=b.ceiling??-1;} else {av=a.cells[key]??-1;bv=b.cells[key]??-1;}
    return sortDir*(av-bv); }); return r; }
function render(){
  document.getElementById('sub').textContent = `${DATA.rows.length} tasks × ${DATA.cols.length} columns`;
  const head=document.getElementById('head');
  const cols=['task','web','ceiling',...DATA.cols];
  head.innerHTML = cols.map(c=>`<th data-k="${c}">${c.replace('baseline-cheap-head','cheap-head')}</th>`).join('');
  head.querySelectorAll('th').forEach(th=>th.onclick=()=>{const k=th.dataset.k;
    if(sortKey===k)sortDir*=-1;else{sortKey=k;sortDir=(k==='task'||k==='web')?1:-1;}render();});
  const body=document.getElementById('body');
  body.innerHTML = rowsFor().map(r=>{
    const cells = DATA.cols.map(m=>{const v=r.cells[m];
      return `<td style="background:${heat(v)}">${fmt(v)}</td>`;}).join('');
    return `<tr><td class="task">${r.task}</td><td><span class="pill ${r.web}">${r.web}</span></td>`+
      `<td class="muted">${r.ceiling==null?'-':r.ceiling.toFixed(2)}</td>${cells}</tr>`;}).join('');
}
function filters(){ const f=document.getElementById('filters'); f.innerHTML='';
  ['all','rich','mixed','zero'].forEach(w=>{const b=document.createElement('button');
    b.textContent=w; if(w===webFilter)b.className='on';
    b.onclick=()=>{webFilter=w;filters();render();}; f.appendChild(b);}); }
filters(); render();
</script></body></html>
"""


def main():
    models = []
    for d in sorted(glob.glob(os.path.join(OUT, "*"))):
        sc = os.path.join(d, "scorecard.json")
        name = os.path.basename(d)
        if name != "dry" and os.path.isfile(sc):
            models.append((name, json.load(open(sc))))
    order = sorted(models, key=lambda x: (x[0] != "baseline-cheap-head", x[0]))
    cols = [m for m, _ in order]
    tasks = sorted({t for _, sc in models for t in sc})
    rows = []
    for t in tasks:
        web = next((sc[t].get("web_exposure", "") for _, sc in order if t in sc), "")
        ceil = next((sc[t]["ceiling"] for _, sc in order if t in sc and sc[t].get("ceiling") is not None), None)
        rows.append({"task": t, "web": web, "ceiling": ceil,
                     "cells": {m: (sc[t]["output_auroc"] if t in sc else None) for m, sc in order}})
    html = TEMPLATE.replace("/*DATA*/", json.dumps({"cols": cols, "rows": rows}))
    open(os.path.join(ROOT, "docs", "leaderboard.html"), "w").write(html)
    print(f"wrote docs/leaderboard.html ({len(tasks)} tasks, {len(cols)} columns)")


if __name__ == "__main__":
    main()
